print("Welcome to our ageCalculator world")
age = int(input('What is your current age?  '))
remainingYear = 90 - age
remainingMonth = remainingYear*12
remainingWeek = remainingYear*52
remainingDays = remainingYear*365
print(f"Your remaining Year is {remainingYear},Your remaining days is {remainingDays} Your remaining Month is {remainingMonth}, Your remaining Week is {remainingWeek} ")